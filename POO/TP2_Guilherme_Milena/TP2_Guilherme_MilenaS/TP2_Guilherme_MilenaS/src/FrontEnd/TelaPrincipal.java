    private void btnAdicionarDisciplinasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarDisciplinasActionPerformed
        if (alunoAtual == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um aluno primeiro.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        String nomeDisciplina = txtDisciplinas.getText().trim();
        String notaStr = txtNotaFinal.getText().trim();

        if (nomeDisciplina.isEmpty() || notaStr.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Preencha o nome da disciplina e a nota final.", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        float nota;
        try {
            nota = Float.parseFloat(notaStr.replace(',', '.'));
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nota inválida. Use números (ex: 75.5).", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean sucesso = alunoAtual.setMatricularDisciplina(nomeDisciplina, nota);
        if (sucesso) {
            txtCadastro.setText(String.format("%.2f", alunoAtual.getCR()));
            txtDisciplinas.setText("");
            txtNotaFinal.setText("");
            javax.swing.JOptionPane.showMessageDialog(this, "Disciplina '" + nomeDisciplina + "' adicionada!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Disciplina '" + nomeDisciplina + "' já está cadastrada.", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAdicionarDisciplinasActionPerformed


    private void btnVerDiciplinasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerDiciplinasActionPerformed
        if (alunoAtual == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um aluno primeiro.", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        String disciplinas = alunoAtual.getDisciplinasMatriculadas();
        if (disciplinas.isEmpty()) {
            disciplinas = "Não existem disciplinas matriculadas";
        }
        javax.swing.JOptionPane.showMessageDialog(this,
            disciplinas + "\nCR: " + String.format("%.2f", alunoAtual.getCR()),
            "Disciplinas de " + alunoAtual.getNome(),
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnVerDiciplinasActionPerformed


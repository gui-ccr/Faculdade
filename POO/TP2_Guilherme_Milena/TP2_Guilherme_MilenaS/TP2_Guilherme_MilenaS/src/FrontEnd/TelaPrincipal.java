    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = txtNomeAluno.getText().trim();
        String cpf = btnCadastrar.getText().trim();

        if (nome.isEmpty() || cpf.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Preencha o nome e o CPF do aluno.", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        Aluno novoAluno = new Aluno(proximaMatricula++, nome, cpf);
        alunos.add(novoAluno);
        cbAlunos.addItem(nome);
        cbAlunos.setSelectedItem(nome);

        txtNomeAluno.setText("");
        btnCadastrar.setText("");

        javax.swing.JOptionPane.showMessageDialog(this, "Aluno '" + nome + "' cadastrado com sucesso!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void cbAlunosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbAlunosActionPerformed
        int idx = cbAlunos.getSelectedIndex();
        if (idx < 0 || idx >= alunos.size()) return;

        alunoAtual = alunos.get(idx);
        txtMatricula.setText(String.valueOf(alunoAtual.getMatricula()));
        txtCPF.setText(alunoAtual.getCPF());
        txtCadastro.setText(String.format("%.2f", alunoAtual.getCR()));
    }//GEN-LAST:event_cbAlunosActionPerformed

